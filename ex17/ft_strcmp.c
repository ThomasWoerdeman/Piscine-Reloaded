int		ft_strcmp(char *s1, char *s2)
{
	int cnt;

	cnt = 0;
	while (s1[cnt] != '\0' && s2[cnt] != '\0' && s1[cnt] == s2[cnt])
		cnt++;
	return (s1[cnt] - s2[cnt]);
}
